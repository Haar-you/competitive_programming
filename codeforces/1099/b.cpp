template <typename T> int sign(T n){return (n>0)-(n<0);}
template <typename T> T ceilGE(T a, T b){if(a % b) return a/b+1-(sign(a)*sign(b)<0?1:0); return a/b;}

int main(){
  cin.tie(0);
  ios::sync_with_stdio(false);

  LLI n; cin >> n;

  LLI ans = 1e18;
  for(LLI i=1; i*i<=n; ++i){
    LLI j = ceilGE(n,i);
    ans = min(ans, i+j);
  }
  cout << ans << endl;

  return 0;
}
