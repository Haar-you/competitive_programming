int main(){
  cin.tie(0);
  ios::sync_with_stdio(false);

  int n;
  while(cin >> n){
    vector<int> a(n); cin >> a;

    sort(ALL(a));
    map<int,int> cnt;
    for(auto x : a) ++cnt[x];

    bool no = false;
    for(auto &kv : cnt) if(kv.snd>=3) no = true;

    vector<int> ans1, ans2;

    for(auto &kv : cnt){
      if(kv.snd == 2){
	ans1.push_back(kv.fst);
	ans2.push_back(kv.fst);
      }else{
	ans1.push_back(kv.fst);
      }
    }

    if(no){
      cout << "NO" << endl;
    }else{
      cout << "YES" << endl;
      cout << ans1.size() << endl;
      join(cout, ALL(ans1));
      cout << ans2.size() << endl;
      reverse(ALL(ans2));
      join(cout, ALL(ans2));
    }
  }
  
  return 0;
}
